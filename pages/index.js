import dynamic from 'next/dynamic';
import Head from 'next/head';
import { createRef, useEffect, useRef, useState } from 'react';
const ListCard = dynamic(() => import('../components/ListCard'));

const Home = ({ data }) => {
  const [cats, setCats] = useState(data.slice(0, 10));
  const [expand, setExpand] = useState();
  const [loading, setLoading] = useState(false);

  function handleScroll() {
    if (
      window.innerHeight + document.documentElement.scrollTop !==
      document.documentElement.offsetHeight
    )
      return;
    setLoading(true);
  }

  function fetchMoreListItems() {
    setTimeout(() => {
      setCats([...cats, ...data.slice(cats.length, cats.length + 10)]);
      setLoading(false);
    }, 500);
  }

  useEffect(() => {
    window.addEventListener('scroll', handleScroll);
    return () => window.removeEventListener('scroll', handleScroll);
  }, []);

  useEffect(() => {
    if (!loading) return;
    fetchMoreListItems();
  }, [loading]);

  return (
    <div className='main'>
      <Head>
        <title>Create Next App</title>
        <meta name='description' content='Generated by create next app' />
        <link rel='icon' href='/favicon.ico' />
      </Head>
      <div className='min-h-screen w-8/12 mx-auto break-words'>
        {cats.map((cat) => {
          return (
            <ListCard
              key={cat.id}
              data={cat}
              expand={expand}
              setExpand={setExpand}
            />
          );
        })}
      </div>
      {loading && cats.length < data.length && (
        <div className='h-12 w-8/12 mx-auto grid justify-items-center py-3'>
          <p className='animate-pulse font-semibold'>Fetching more data...</p>
        </div>
      )}
    </div>
  );
};

// This gets called on every request
export async function getServerSideProps() {
  // Fetch data from external API
  const res = await fetch(`https://api.thecatapi.com/v1/breeds`);
  const data = await res.json();

  // Pass data to the page via props
  return { props: { data } };
}

export default Home;
